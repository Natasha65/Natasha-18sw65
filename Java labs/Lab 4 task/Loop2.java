class Loop2
{
	public static void main(String [] args)
	{
		   int a=0;
			   while(a<=10)
			   {
			   	System.out.println(a);
			   	a+=2;

			   }
			   for(int i=1 ; i<=10 ;i++)
			   {
			   	 if(i%2!=0)
			   	 {
			   		System.out.println("\n" +i);
			   
			   	 }
			   		i++;
			   }

		
	}
}
